sl="*++++++++++++++++++++++++++++++++++++++++++++*"
print(sl)
ls=list(sl)
i=len(sl)-1

k=1
t=((('*',-1,2),('+',1,1)),
(('*',0,0),('+',-1,3)),
(('*',0,0),('-',-1,2)))
while k:
    if ls[i]=="*":
        ls[i]=t[k-1][0][0]
        i+=t[k-1][0][1]
        k=t[k-1][0][2]
    else:
        ls[i]=t[k-1][1][0]
        i+=t[k-1][1][1]
        k=t[k-1][1][2]
sl="".join(ls)
print(sl)
